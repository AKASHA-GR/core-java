class Necklace {
    String brand;
    String material;
    String color;
    double price;
    double weight;
    String lengthType;
    String design;
    boolean adjustable;
    String claspType;
    String stoneType;
    int stoneCount;
    boolean isGoldPlated;
    String originCountry;
    String shape;
    double length;
    double width;
    String finish;
    boolean waterproof;
    String warranty;
    String occasion;

    Necklace(String brand, String material, String color, double price, double weight,
             String lengthType, String design, boolean adjustable, String claspType,
             String stoneType, int stoneCount, boolean isGoldPlated, String originCountry,
             String shape, double length, double width, String finish,
             boolean waterproof, String warranty, String occasion) {

        this.brand = brand;
        this.material = material;
        this.color = color;
        this.price = price;
        this.weight = weight;
        this.lengthType = lengthType;
        this.design = design;
        this.adjustable = adjustable;
        this.claspType = claspType;
        this.stoneType = stoneType;
        this.stoneCount = stoneCount;
        this.isGoldPlated = isGoldPlated;
        this.originCountry = originCountry;
        this.shape = shape;
        this.length = length;
        this.width = width;
        this.finish = finish;
        this.waterproof = waterproof;
        this.warranty = warranty;
        this.occasion = occasion;
    }
}